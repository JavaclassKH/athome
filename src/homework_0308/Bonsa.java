package homework_0308;

public abstract class Bonsa {
	private String KimchiStew;
	private String BudaeStew;
	private String BibimBab;
	private String SundaeStew;
	private String Rice;
	
	public void price() {}

	public String getKimchiStew() {
		return KimchiStew;
	}

	public void setKimchiStew(String kimchiStew) {
		KimchiStew = kimchiStew;
	}

	public String getBudaeStew() {
		return BudaeStew;
	}

	public void setBudaeStew(String budaeStew) {
		BudaeStew = budaeStew;
	}

	public String getBibimBab() {
		return BibimBab;
	}

	public void setBibimBab(String bibimBab) {
		BibimBab = bibimBab;
	}

	public String getSundaeStew() {
		return SundaeStew;
	}

	public void setSundaeStew(String sundaeStew) {
		SundaeStew = sundaeStew;
	}

	public String getRice() {
		return Rice;
	}

	public void setRice(String rice) {
		Rice = rice;
	}
	
	
	
	
	
	
	
}
